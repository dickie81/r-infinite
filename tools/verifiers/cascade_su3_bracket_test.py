    print("=" * 78)
    print("CASCADE LIE BRACKET COMPUTATION AT d=12")
    print("=" * 78)
    print()
    print("Setup: R^12 = H^3 (three quaternions).")
    print("Adams fields at d=12: right-mult by quaternion i, j, k on H^3.")
    print("Cascade J: left-mult by quaternion i on H^3.")
    print()

    # Construct generators
    Vi_local = quat_right_mult_i()
    Vj_local = quat_right_mult_j()
    Vk_local = quat_right_mult_k()
    Ji_local = quat_left_mult_i()

    # Verify local quaternion algebra
    print("=" * 78)
    print("Step 1: Verify local quaternion algebra on H = R^4")
    print("=" * 78)
    print()

    # Check skew-symmetry
    for name, M in [("V_R^i", Vi_local), ("V_R^j", Vj_local),
                     ("V_R^k", Vk_local), ("J_L^i", Ji_local)]:
        is_skew = np.allclose(M + M.T, 0)
        print(f"  {name} skew-symmetric: {is_skew}")

    # Check J^2 = -I
    print(f"  J_L^i^2 = -I: {np.allclose(Ji_local @ Ji_local, -np.eye(4))}")

    # Check quaternion algebra: ij = k, jk = i, ki = j (right-mult composition)
    # Right-mult by i then j: x*i*j = x*(ij) = x*k -> matrix product Vj * Vi
    print()
    print("  Quaternion algebra (right-mult compositions):")
    Vij = Vj_local @ Vi_local
    print(f"    V_R^j @ V_R^i (= right-mult by ij = k): equals V_R^k? "
          f"{np.allclose(Vij, Vk_local)}")
    Vjk = Vk_local @ Vj_local
    print(f"    V_R^k @ V_R^j (= right-mult by jk = i): equals V_R^i? "
          f"{np.allclose(Vjk, Vi_local)}")
    Vki = Vi_local @ Vk_local
    print(f"    V_R^i @ V_R^k (= right-mult by ki = j): equals V_R^j? "
          f"{np.allclose(Vki, Vj_local)}")
    print()

    # Brackets among right-mults: should give 2x the quaternion product
    # [V_R^i, V_R^j] = V_R^i V_R^j - V_R^j V_R^i
    # V_R^i V_R^j = right-mult by ji = -k, so V_R^i V_R^j = -V_R^k
    # V_R^j V_R^i = right-mult by ij = k, so V_R^j V_R^i = V_R^k
    # So [V_R^i, V_R^j] = -V_R^k - V_R^k = -2 V_R^k
    print("  Brackets [V_R^i, V_R^j]:")
    bij = bracket(Vi_local, Vj_local)
    print(f"    [V_R^i, V_R^j] = -2 V_R^k? {np.allclose(bij, -2 * Vk_local)}")
    bjk = bracket(Vj_local, Vk_local)
    print(f"    [V_R^j, V_R^k] = -2 V_R^i? {np.allclose(bjk, -2 * Vi_local)}")
    bki = bracket(Vk_local, Vi_local)
    print(f"    [V_R^k, V_R^i] = -2 V_R^j? {np.allclose(bki, -2 * Vj_local)}")
    print()
    print("  Note: brackets close as su(2) (3-dim non-Abelian Lie algebra).")
    print()

    # Now check J vs V brackets
    print("=" * 78)
    print("Step 2: J commutes with right-mult fields (left-right commutativity)")
    print("=" * 78)
    print()

    for name, V in [("V_R^i", Vi_local), ("V_R^j", Vj_local), ("V_R^k", Vk_local)]:
        commutator = bracket(Ji_local, V)
        commutes = np.allclose(commutator, 0)
        print(f"  [J_L^i, {name}] = 0? {commutes}")
    print()
    print("  Quaternionic left and right multiplications COMMUTE.")
    print("  This is a standard fact (the algebra of right multiplications")
    print("  commutes with the algebra of left multiplications).")
    print()

    # Build full 12x12 generators
    Vi = block_diag_n(Vi_local, 3)
    Vj = block_diag_n(Vj_local, 3)
    Vk = block_diag_n(Vk_local, 3)
    J = block_diag_n(Ji_local, 3)

    # Compute Lie algebra generated by {Vi, Vj, Vk, J}
    print("=" * 78)
    print("Step 3: Lie algebra generated by {V_R^i, V_R^j, V_R^k, J_L^i} on R^12")
    print("=" * 78)
    print()

