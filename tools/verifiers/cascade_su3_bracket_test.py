    print("=" * 78)
    print("Step 4: comparison with su(3) requirement")
    print("=" * 78)
    print()
    print(f"  Cascade's d=12 algebra (Adams + J): dim {dim}")
    print(f"  Required for SU(3): dim 8")
    print(f"  Gap: {8 - dim} generators missing")
    print()

    if dim == 4:
        print("  CONFIRMED: cascade-internal structure at d=12 generates u(2),")
        print("  not su(3).  The 4 missing generators (off-diagonal Gell-Mann")
        print("  matrices that mix the 3 H factors) are NOT supplied by Adams +")
        print("  cascade J.")
        print()
        print("  Where could they come from?")
        print("  - Permutations of the 3 H factors: 3-dim symmetric group action.")
        print("    But these aren't 'cascade-internal' in any forced sense -- they")
        print("    require choosing a labelling of the 3 quaternions.")
        print("  - SO(12) Killing fields at large: cascade has access to all 66")
        print("    so(12) generators.  Selecting an su(3) embedding requires")
