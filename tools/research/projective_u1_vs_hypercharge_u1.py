def radon_hurwitz(n: int) -> int:
    """Radon--Hurwitz number rho(n).

    Write n = 2^a * m with m odd; a = 4q + r, 0 <= r <= 3.
    rho(n) = 8q + 2^r.  Adams' theorem: max number of linearly independent
    nowhere-zero tangent vector fields on S^{n-1} is rho(n) - 1.
    """
    if n <= 0:
        raise ValueError("n must be positive")
    a, m = 0, n
    while m % 2 == 0:
        a += 1
        m //= 2
    q, r = divmod(a, 4)
    return 8 * q + 2**r


def R_cascade(d: int) -> float:
    """Cascade radius R(d) = Gamma((d+1)/2) / Gamma((d+2)/2)."""
    return float(np.exp(gammaln((d + 1) / 2.0) - gammaln((d + 2) / 2.0)))


def alpha_cascade(d: int) -> float:
    """Cascade gauge coupling alpha(d) = R(d)^2 / 4 = N(d)^2 / (4 pi)."""
    return R_cascade(d) ** 2 / 4.0


def build_J(n: int) -> np.ndarray:
    """Block-diagonal J on R^{2n}: each 2x2 block is [[0,-1],[1,0]].

    This is the cascade complex structure of Part II thm:complex,
    extended to all n precessing pairs of axes (Part II proof of
    thm:complex: 'Extending to all pairs of precessing axes
    partitions R^{2n} into n complex dimensions C^n').
    """
    J = np.zeros((2 * n, 2 * n))
    for k in range(n):
        J[2 * k, 2 * k + 1] = -1.0
        J[2 * k + 1, 2 * k] = +1.0
    return J


