    J = build_J(n)
    rng = np.random.default_rng(seed)

    # Sanity: J^2 = -I  (Part II thm:complex)
    JJ = J @ J
    assert np.allclose(JJ, -np.eye(dim))
    print(f"Cascade J on R^{dim} = R^{{2*{n}}}: built block-diagonal.")
    print(f"  J^2 = -I check:  ||J^2 + I||_F = {np.linalg.norm(JJ + np.eye(dim)):.2e}  OK")
    print()

    # ---- (G1) Tangency: <Jx, x> = 0 -------------------------------------
    print("(G1) TANGENCY.  For every x in S^13, is Jx tangent to the sphere")
    print("     at x?  Tangency at x means <Jx, x> = 0.  Since J is")
    print("     antisymmetric (J^T = -J), <Jx,x> = -<x,Jx> = -<Jx,x>, so")
    print("     <Jx,x> = 0 identically.  Numerical check on 10000 random")
    print("     unit vectors:")
    N_samples = 10000
    X = rng.standard_normal((N_samples, dim))
    X /= np.linalg.norm(X, axis=1, keepdims=True)
    JX = X @ J.T   # Jx for each x as rows
    inner = np.einsum("ij,ij->i", JX, X)
    print(f"     max |<Jx,x>| over {N_samples} samples: {np.max(np.abs(inner)):.2e}  (zero to roundoff)")
    print()

