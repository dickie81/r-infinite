    group_err = np.linalg.norm(R1 @ R2 - R_sum)
    print(f"     max |x(theta)| - 1: {max(norm_errs):.2e}")
    print(f"     ||R(t1) R(t2) - R(t1+t2)||_F: {group_err:.2e}")
    print()
    print("     Conclusion (G): the Adams U(1) on S^13 IS the U(1) generated")
    print("     by J|_{S^13}.  This closes the gauge-GROUP identity question.")
