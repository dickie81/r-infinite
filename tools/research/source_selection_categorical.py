# =============================================================================
# 1. CASCADE OBSERVABLE ALGEBRA
# =============================================================================
# Generators:
#   - N(d)              : cascade lapse at layer d (positive integer)
#   - Omega_d           : sphere area at layer d
#   - Phi(d)            : cumulative cascade potential, sum of log N(j) for j <= d
#   - Phi(d_A -> d_B)   : descent transition, Phi(d_B) - Phi(d_A)
#   - p(d)              : descent probability at d
#   - M_Pl_red          : reduced Planck mass (the dimensionful anchor)
#   - K (constants)     : numerical constants (sqrt(pi), chi=2, N_c=3, e, etc.)
# Operations:
#   - composition: +, -, *, /, ^
#   - exponentiation: exp(.)
#   - trigonometric: sin, cos, tan, arctan (for mixing angles)
#
# An OBSERVABLE FORMULA is an element of the free algebra over these generators
# modulo cascade-specific identities (e.g., 2*sqrt(pi) = N(0)*Gamma(1/2)).


class GeneratorKind(Enum):
    """Atomic cascade primitives appearing in observable formulas."""
    N_d = "N(d)"                  # lapse, layer-supported at d
    OMEGA_d = "Omega_d"            # sphere area, layer-supported at d
    PHI_d = "Phi(d)"               # cumulative potential up to d
    PHI_AB = "Phi(d_A->d_B)"       # descent transition
    P_d = "p(d)"                   # descent probability at d
    M_PL = "M_Pl_red"              # Planck mass; layer-support {217}
    CONSTANT = "K"                 # numerical constant; layer-support {}


@dataclass
class FormulaSummary:
    """Algebraic summary of a cascade observable formula.

    For the categorical derivation we track only the membership-relevant
    properties of the formula's symbol content -- not the full expression tree.
    """
    name: str
    expression: str                       # canonical form for human reading
    has_M_Pl: bool                        # contains M_Pl_red (directly or via subexpression)
    has_phi_exp: bool                     # contains exp(Phi(.)) or exp(Phi(d_A->d_B))
    has_p_factor: bool                    # contains p(d) factor
    gw_descent: bool                      # contains exp(Phi(d_A->d_B)) with [d_A,d_B] crossing {12,13,14} and d_B-d_A>=2
    layer_support: Set[int] = field(default_factory=set)


# =============================================================================
# 2. SUB-ALGEBRAS
# =============================================================================
# A_Pl     = sub-algebra generated by everything EXCEPT M_Pl_red.
# A_static = sub-algebra generated by {N(d), Omega_d, K}, excluding Phi, p, M_Pl
#            (no descent of any kind, no Planck anchor).
# A_noGW   = sub-algebra of formulas WITHOUT a multi-layer gauge-window descent
#            factor exp(Phi(d_A->d_B)) where (d_B - d_A) >= 2 and the path crosses
#            the gauge window {12, 13, 14}.
#
# These three sub-algebras define three Boolean functors on observable formulas:
#   F1: gauge content     (NOT in A_noGW)
#   F2: Planck content    (NOT in A_Pl)
#   F3: static content    (in A_static)


def F1_gauge(f: FormulaSummary) -> bool:
    """F1 = formula contains a multi-layer gauge-window descent factor."""
    return f.gw_descent


def F2_planck(f: FormulaSummary) -> bool:
    """F2 = formula contains M_Pl_red (anywhere, directly or via subexpression)."""
    return f.has_M_Pl


def F3_static(f: FormulaSummary) -> bool:
    """F3 = formula is in the static sub-algebra (no Phi/p/exp(Phi))."""
    return not (f.has_phi_exp or f.has_p_factor)


# =============================================================================
# 3. STRUCTURAL CONSTRAINT (algebraic theorem)
# =============================================================================

def constraint_gauge_implies_nonstatic(f: FormulaSummary) -> bool:
    """
    Theorem (algebraic): F1(f) = T implies F3(f) = F.

    Proof: F1(f) = T means f contains exp(Phi(d_A->d_B)) with the path crossing
    the gauge window.  This is a Phi-exponential factor, so f.has_phi_exp = True.
    Therefore F3(f) = NOT (has_phi_exp OR has_p_factor) = F.
    """
    if F1_gauge(f):
        return not F3_static(f)
    return True  # vacuously true if F1 is False


# =============================================================================
# 4. EXHAUSTIVENESS THEOREM
# =============================================================================
# Let (F1, F2, F3) take all 2^3 = 8 boolean values.  By the constraint above,
# F1 = T forces F3 = F.  So (T, *, T) for any * is empty: 2 cases ruled out.
# The remaining 6 cases are partitioned by the decision procedure:
#
#   if F2:                          # Absolute (covers cases T_Tx and F_Tx)
#       d* = d_1 = 19
#   elif F3:                        # Observer (covers F_FT only since T_FT empty)
#       d* = d_V = 5
#   elif F1:                        # Gauge (covers T_FF only since T_FT empty)
#       d* = d_gw = 14
#   else:                           # Amplitude (covers F_FF)
#       d* = d_0 = 7
#
# Exactly 4 outputs.  No fifth class is reachable without expanding the
# cascade primitive set.


class SourceClass(Enum):
    ABSOLUTE = ("Absolute", 19)
    OBSERVER = ("Observer", 5)
    GAUGE = ("Gauge", 14)
    AMPLITUDE = ("Amplitude", 7)


def classify(f: FormulaSummary) -> SourceClass:
    """Decision procedure -- short-circuit evaluation."""
    if F2_planck(f):
        return SourceClass.ABSOLUTE
    if F3_static(f):
        return SourceClass.OBSERVER
    if F1_gauge(f):
        return SourceClass.GAUGE
    return SourceClass.AMPLITUDE


def prove_exhaustiveness() -> tuple[bool, list[str]]:
    """Enumerate all 2^3 = 8 boolean triples, apply the constraint, classify,
    and verify that the union of reached classes is exactly {Absolute, Observer,
    Gauge, Amplitude} and that no unreachable case maps to a fifth class."""
    cases_reached: dict[SourceClass, list[tuple[bool, bool, bool]]] = {
        c: [] for c in SourceClass
    }
    empty_cases: list[tuple[bool, bool, bool]] = []
    for f1 in [False, True]:
        for f2 in [False, True]:
            for f3 in [False, True]:
                # Test the constraint: F1=T => F3=F
                if f1 and f3:
                    empty_cases.append((f1, f2, f3))
                    continue
                # Apply decision procedure
                if f2:
                    cls = SourceClass.ABSOLUTE
                elif f3:
                    cls = SourceClass.OBSERVER
                elif f1:
                    cls = SourceClass.GAUGE
                else:
                    cls = SourceClass.AMPLITUDE
                cases_reached[cls].append((f1, f2, f3))
    notes = []
    notes.append(
        f"Empty cases (forbidden by constraint F1=T => F3=F): {empty_cases}"
    )
    for cls, triples in cases_reached.items():
        notes.append(f"  Class {cls.value[0]:>10}: reached by {triples}")
    all_reached = all(len(triples) >= 1 for triples in cases_reached.values())
    return all_reached, notes


